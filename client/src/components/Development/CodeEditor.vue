        let timeDiffTag = `<span class="text-good">${lineParts[2]}</span>`
        let logContent = htmlEscaper.escape(lineParts.slice(3).join(' '));

        return [timeTag, timeDiffTag, logContent].join(' ');
      });
      printLogs = printLogs.join('\n') || null;

