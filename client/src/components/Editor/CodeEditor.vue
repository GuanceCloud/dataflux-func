    scriptOutputText() {
      let sections = [];
      this.scriptOutput.forEach(o => {
        // 分割线
        let divider = `<span class="code-editor-output-info">#<span class="code-editor-output-seq">${o.seq}</span> ${'-'.repeat(20)}</span>`;

        // 标题
        let titleLabel = '';
        switch(o.type) {
          case 'publish':
            titleLabel = this.$t('Publish Script') + this.$t(':');
            break;

          case 'execute':
            titleLabel = this.$t('Executed Func') + this.$t(':');
            break;
        }
        let title = `<span class="code-editor-output-info">${titleLabel} ${o.name}</span>`;

        // 执行内存消耗
        let peakMemoryUsageInfo = '';
        if (o.peakMemroyUsage) {
          peakMemoryUsageInfo = `<span class="code-editor-output-info">${this.$t('Peak Memory Allocated')}${this.$t(':')} ${this.T.byteSizeHuman(o.peakMemroyUsage)}</span>`;
        }

        // 执行耗时
        let costInfo = '';
        if (o.cost) {
          costInfo = `<span class="code-editor-output-info">${this.$t('Run Time')}${this.$t(':')} ${this.$tc('seconds', o.cost)}</span>`;
          if (o.cost > 3) {
            costInfo += `<br>&emsp;<span class="text-watch">${this.$t('It took too much time for running (more than 3s), may not be suitable for synchronous calling scenario')}</span>`;
          }
        }

        // 日志输出
        // 【已经经过预处理，此处已经是纯文本，无需进一步处理】
        let logMessages = o.logMessages;

        // 函数输出
        // 【已经经过预处理，此处已经是纯文本，无需进一步处理】
        let funcOutput = o.funcOutput;
        let funcOutputTitle = null;
        if (funcOutput) {
          funcOutputTitle = `<span class="code-editor-output-info">${this.$t('Func Return Value (repr)')}${this.$t(':')}</span>`;
        }

        // 错误堆栈
        // 【已经经过预处理，此处已经是纯文本，无需进一步处理】
        let stackInfo = o.stackInfo;
        let stackInfoTitle = null;
        if (stackInfo) {
          stackInfoTitle = `<span class="code-editor-output-info">${this.$t('Stack')}${this.$t(':')}</span>`
        }

        let section = [ divider, title ]
        if (logMessages) {
          section.push('', logMessages);
        }
        if (funcOutputTitle) {
          section.push('', funcOutputTitle, funcOutput);
        }
        if (costInfo || peakMemoryUsageInfo) {
          section.push('');
          if (peakMemoryUsageInfo) section.push(peakMemoryUsageInfo);
          if (costInfo)            section.push(costInfo);
        }
        if (stackInfoTitle) {
          section.push('', stackInfoTitle,  stackInfo);
        }

        // 过滤空白内容
        section = section.filter(x => 'string' === typeof x).join('\n').trimStart();

        sections.push(section);
      });

      let sectionText = sections.join('\n\n');

      // 限制输出总量
      let lines = sectionText.split('\n');
      if (lines.length > this.TEXT_OUTPUT_LIMIT) {
        sectionText = lines.slice(-1 * this.TEXT_OUTPUT_LIMIT).join('\n');
      }

      return sectionText;
    },

    isValidFuncCallKwargsJSON() {
      // 允许空白
      if (this.funcCallKwargsJSON.trim() === '') {
        return true;
      }

      // 必须为字典
      if (this.funcCallKwargsJSON.trim()[0] !== '{') {
        return false;
      }

      // 必须可解析JSON
      let _parsed = null
      try {
        _parsed = JSON.parse(this.funcCallKwargsJSON)
      } catch(e) {
        return false;
      }

      return true;
    },

    selectedItem() {
      if (!this.selectedItemId) return null;

      for (let i = 0; i < this.selectableItems.length; i++) {
        let _item = this.selectableItems[i];
        if (_item.id === this.selectedItemId) {
          return _item;
        }
      }
    },
    isFuncSelected() {
      if (!this.selectedItem) return false;
      return this.selectedItem.type === 'def';
    },
