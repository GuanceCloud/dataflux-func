    updateSelectableItems() {
      if (!this.data.codeDraft) return;

      let todoItems = [];
      let codeItems = [];
      this.data.codeDraft.split('\n').forEach((l, i) => {
        try {
          // 注释项目
          this.C.TODO_TYPE.forEach(x => {
            let _tag = `# ${x.key}`;
            let _pos = l.indexOf(_tag);
            if (_pos >= 0) {
