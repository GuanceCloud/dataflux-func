        // 【已经经过预处理，此处已经是纯文本，无需进一步处理】
        let logMessages = o.logMessages;

        // 函数输出
        // 【已经经过预处理，此处已经是纯文本，无需进一步处理】
        let funcOutput = o.funcOutput;
        let funcOutputTitle = null;
        if (funcOutput) {
          funcOutputTitle = `<span class="code-editor-output-info">${this.$t('Func Return Value:')}</span>`;
        }

        // 错误堆栈
        // 【已经经过预处理，此处已经是纯文本，无需进一步处理】
        let stackInfo = o.stackInfo;
        let stackInfoTitle = null;
        if (stackInfo) {
          stackInfoTitle = `<span class="code-editor-output-info">${this.$t('Stack:')}</span>`
        }

        let section = [
          divider,
          title,
          costInfo,    '',
          logMessages, '',
          funcOutputTitle, funcOutput,
          stackInfoTitle, stackInfo,
        ];

        // 过滤空白内容
        section = section.filter(x => 'string' === typeof x).join('\n').trimStart();

        sections.push(section);
      });

      let sectionText = sections.join('\n\n');

      // 限制输出总量
      let lines = sectionText.split('\n');
      if (lines.length > this.TEXT_OUTPUT_LIMIT) {
        sectionText = lines.slice(-1 * this.TEXT_OUTPUT_LIMIT).join('\n');
      }

      return sectionText;
    },
  },
  props: {
  },
  data() {
    return {
      isNewLoaded: true,
      codeMirror: null,

      highlightedLineInfoMap: {},

      currentErrorLineInfo: null,

      data     : {},
      scriptSet: {},

      draftFuncs        : [],
      selectedFuncId    : '',
      funcCallKwargsJSON: '',
      funcCallSeq       : 0,

      countDownTimer       : null,
      fullScreenLoading    : false,
      workerRunning        : false,
      workerRunningTipTitle: '',

      // 文本输出
      scriptOutput: [],

      // 用于乐观锁
      prevCodeDraftMD5: null,

      // 代码保存中标志位
      isSavingCodeDraft: false,

      // DIFF信息
      diffAddedCount  : 0,
      diffRemovedCount: 0,
    }
  },
  created() {
    this.debouncedUpdatePanePercent = this.T.debounce((store, nextPercent) => {
      store.commit('updateCodeEditor_splitPanePercent', nextPercent);
    }, 100);
  },
  mounted() {
    setImmediate(() => {
      // 初始化编辑器
      this.codeMirror = this.T.initCodeMirror('editor_CodeEditor');
      this.codeMirror.setOption('theme', this.codeMirrorTheme);
    });
  },
  async beforeRouteLeave(to, from, next) {
    // 清除所有高亮
    this.updateHighlightLineConfig('selectedFuncLine', null);
    this.updateHighlightLineConfig('errorLine', null);

    if (this.isLockedByOther) {
      return next();
    }
    if (!this.codeMirror) {
      return next();
    }
    if (!this.$store.getters.isSignedIn) {
      return next();
    }

    // 保存
    let apiRes = await this._saveCodeDraft({ mute: true });
    if (apiRes && apiRes.ok) {
      return next();
    }
  },
}
