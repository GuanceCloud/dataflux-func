              todoItems.push({
                id      : id,
                type    : 'todo',
                todoType: x.key,
                name    : name,
                line    : i,
              })
            }
          })

          // 代码项目
          if (l.indexOf('def ') === 0 && l.indexOf('def _') < 0) {
            // 函数定义
            let _parts = l.slice(4).split('(');

            let name = _parts[0];
            let id   = `${this.scriptId}.${name}`;

            let kwargs = _parts[1].slice(0, -2).split(',').reduce((acc, x) => {
