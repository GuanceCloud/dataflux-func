        this.codeMirror.setCursor({line: this.codeMirror.lineCount() - 1});
        this.codeMirror.setCursor({line: scrollTo});
      } else if (options.marginType === 'prev') {
        // 向上留余地，回顶下滚
        this.codeMirror.setCursor({line: 0});
        this.codeMirror.setCursor({line: scrollTo});
      }

