              <span class="error-info">
                <i class="fa fa-fw fa-times-circle"></i>
                <span>${errorText}</span>
              </span> `;

          this.updateHighlightLineConfig('errorLine', {
            line            : errorLine,
            scroll          : 1,
            marginType      : 'prev',
            textClass       : 'highlight-text',
            backgroundClass : 'error-line-background highlight-code-line-blink',
